

/**
 * The class GameArea provides constants which describe various aspects of the
 * area on which Pong is played.
 */
public class GameArea {
    /**
     * left and right borders of GameArea
     */
    public static final int x1 = 15;
    public static final int x2 = 285;
    
    /**
     * top and bottom borders of GameArea
     */
    public static final int y1 = 15;
    public static final int y2 = 185;
    
    /**
     * width and height of GameArea
     */
    public static final int width = x2 - x1;
    public static final int height = y2 - y1;
}
